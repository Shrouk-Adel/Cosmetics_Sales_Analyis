-- update promotion discount value to make it valide to make sal price after promotion always larger than original price 

DELIMITER //

CREATE PROCEDURE adjust_discounts()
BEGIN
  DECLARE done INT DEFAULT 0;
  REPEAT
    UPDATE promotions AS pm
    JOIN products AS p ON pm.product_id = p.product_id
    JOIN sale_details AS sd ON sd.product_id = p.product_id
    SET pm.discount_percent = pm.discount_percent - 5
    WHERE ROUND((sd.unit_price * sd.quantity * (1 - pm.discount_percent / 100)), 2) < (p.original_price * sd.quantity);

    SELECT COUNT(*) INTO done
    FROM promotions AS pm
    JOIN products AS p ON pm.product_id = p.product_id
    JOIN sale_details AS sd ON sd.product_id = p.product_id
    WHERE ROUND((sd.unit_price * sd.quantity * (1 - pm.discount_percent / 100)), 2) < (p.original_price * sd.quantity);

  UNTIL done = 0
  END REPEAT;
END //

DELIMITER ;

CALL adjust_discounts();


