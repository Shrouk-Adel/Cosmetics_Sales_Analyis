CREATE DATABASE cosmetics_sales_db;
USE cosmetics_sales_db;

-- 1. Customers Table
CREATE TABLE Customers (
    customer_id INT AUTO_INCREMENT PRIMARY KEY,
    first_name VARCHAR(50),
    last_name VARCHAR(50),
    gender ENUM('Female', 'Male', 'Other'),
    email VARCHAR(100),
    phone VARCHAR(20),
    city VARCHAR(50),
    join_date DATE
);

-- 2. Products Table
CREATE TABLE Products (
    product_id INT AUTO_INCREMENT PRIMARY KEY,
    product_name VARCHAR(100),
    category VARCHAR(50),
    brand VARCHAR(50),
    price DECIMAL(10,2),
    stock_quantity INT
);

-- 3. Employees Table
CREATE TABLE Employees (
    employee_id INT AUTO_INCREMENT PRIMARY KEY,
    full_name VARCHAR(100),
    role VARCHAR(50),
    hire_date DATE,
    salary DECIMAL(10,2)
);

-- 4. Stores Table
CREATE TABLE Stores (
    store_id INT AUTO_INCREMENT PRIMARY KEY,
    store_name VARCHAR(100),
    city VARCHAR(50),
    manager_id INT,
    FOREIGN KEY (manager_id) REFERENCES Employees(employee_id)
);

-- 5. Sales Table (added sale_time column)
CREATE TABLE Sales (
    sale_id INT AUTO_INCREMENT PRIMARY KEY,
    customer_id INT,
    store_id INT,
    sale_datetime DATETIME DEFAULT CURRENT_TIMESTAMP,  -- date and time of the sale
    total_amount DECIMAL(10,2),
    payment_method ENUM('Cash', 'Credit Card', 'Online'),
    FOREIGN KEY (customer_id) REFERENCES Customers(customer_id),
    FOREIGN KEY (store_id) REFERENCES Stores(store_id)
);

-- 6. Sale_Details Table (Many-to-Many)
CREATE TABLE Sale_Details (
    sale_detail_id INT AUTO_INCREMENT PRIMARY KEY,
    sale_id INT,
    product_id INT,
    quantity INT,
    unit_price DECIMAL(10,2),
    FOREIGN KEY (sale_id) REFERENCES Sales(sale_id),
    FOREIGN KEY (product_id) REFERENCES Products(product_id)
);
-- 7. Promotion
CREATE TABLE Promotions (
    promo_id INT AUTO_INCREMENT PRIMARY KEY,
    product_id INT,
    discount_percent DECIMAL(5,2),
    start_date DATE,
    end_date DATE,
    
    FOREIGN KEY (product_id) REFERENCES Products(product_id)
    
);


